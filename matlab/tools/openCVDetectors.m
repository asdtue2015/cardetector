function [] = writeYAML( name, coeffs, window )

    %% write yml the classifiers file
    fid  = fopen(name,'w');
    fprintf(fid,'%%YAML:1.0\n');  
    fprintf(fid,'width: %i\n',window(1));
    fprintf(fid,'height: %i\n',window(2));
    fprintf(fid,'detector: [ ');   
    for i = 1:1:size(coeffs,1)-1
        fprintf(fid,'%.10d, ',coeffs(i));
    end
    fprintf(fid,'%.10d ]',coeffs(end));
    fclose(fid);

end


